package conversions;

public class Volume {
	
	public static double kiloTometers(double kilo) {
		return kilo * 4 ;
	}
	
	public static double metersToKilo(double meters) {
		return meters / 4 ;
	}
	
	public static double metersToCenti(double meters) {
		return meters * 2 ;
	}
	
	public static double centiToMeters(double meters) {
		return meters / 2 ;
	}
	
}
